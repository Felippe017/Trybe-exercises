function sum(a, b) {
  return a + b;
}

function dim(a, b) {
  return a - b;
}

function mul(a, b) {
  return a * b;
}

function div(a, b) {
  return a / b;
}

function mod(a, b) { 
  return a % b;
}

console.log(sum(5, 4));
console.log(dim(10, 9));
console.log(mul(2, 5));
console.log(div(10, 2));
console.log(mod(5, 4));
